{
  int i;

  for (i = 0; i < HO_ARRAY_IN_SIZE; i++)
    if (array_in[i] < array_data[sign_index][i][0]
	|| array_in[i] > array_data[sign_index][i][1])
      return 0.0;

  return 1.0;
}
